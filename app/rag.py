def _build_prompt(user_q: str, passages: List[Dict[str, Any]]) -> str:
    ctx_lines = []
    for i, p in enumerate(passages, 1):
        snippet = (p["text"] or "").replace("\n", " ").strip()
        if snippet:
            ctx_lines.append(f"[{i}] {snippet}")
    ctx = "\n".join(ctx_lines[:8])

    return (
        f"{SYSTEM}\n\n"
        f"Contexto (fragmentos de la C√°mara de Comercio de Pamplona):\n{ctx}\n\n"
        f"Pregunta del usuario: {user_q}\n\n"
        f"Instrucciones para la respuesta:\n"
        f"- Usa SOLO la informaci√≥n del contexto.\n"
        f"- Si hay horarios, direcciones o tel√©fonos, devu√©lvelos completos y actualizados.\n"
        f"- Responde en un m√°ximo de 5‚Äì7 l√≠neas, formato WhatsApp, usando vi√±etas cuando ayude.\n"
        f"- Si la informaci√≥n no aparece en el contexto, responde que no cuentas con esos datos.\n"
        f"- No inventes enlaces ni promociones."
    )

